package guvi;

public class Guvi {
	public static void main(String [] args){
		int a=8;
		for(int i=0;i<a+1;i++){
			System.out.print("* ");
		}
		System.out.println();
		for(int i=1;i<a;i++){
			System.out.println("*\t\t*");
		}
		for(int i=0;i<a+1;i++){
			System.out.print("* ");
		}
		
	}
}
